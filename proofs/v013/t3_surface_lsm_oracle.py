#!/usr/bin/env python3
"""v0.13 Tier-3 SURFACE-LAYER + LSM operational fp64 pristine-WRF oracle.

Gates the OPERATIONAL, ``jax.jit``-traceable v0.13 Tier-3 surface/LSM kernels
against fp64 savepoints produced by standalone Fortran drivers compiled against
the UNMODIFIED pristine WRF sources (NOT a JAX self-compare):

* ``physics.lsm_slab.slab_columns``        <- ``phys/module_sf_slab.F`` (SLAB)
* ``physics.sfclay_old_mm5.sfclay_old_mm5_columns``
                                           <- ``phys/module_sf_sfclay.F`` (SFCLAY)
* ``physics.sfclay_gfs.sf_gfs_columns``    <- ``phys/module_sf_gfs.F`` (SF_GFS)

The savepoints + per-source SHA-256 + build manifests live under
``proofs/v013/savepoints/surface_lsm/fp64`` and are regenerated by the
``proofs/v013/oracle/surface_lsm/*_build_and_run.sh`` scripts (conda ``wrfbuild``
gfortran, ``-fdefault-real-8 -fdefault-double-8``). The batched kernels are
exercised through ``jax.jit`` over the full case batch to prove the operational
path is genuinely jit/vmap-traceable.

Run (CPU, fp64; the GPU is owned by another lane):

    taskset -c 0-3 env JAX_PLATFORMS=cpu CUDA_VISIBLE_DEVICES= PYTHONPATH=src \\
        python proofs/v013/t3_surface_lsm_oracle.py

Writes ``proofs/v013/t3_surface_lsm_oracle.json``.
"""

from __future__ import annotations

import json
import os
