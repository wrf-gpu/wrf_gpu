def _git_head() -> str:
    return subprocess.check_output(
        ["git", "-C", str(ROOT), "rev-parse", "HEAD"], text=True
    ).strip()


def build_report() -> dict:
    schemes = {
        "slab_lsm": _run_slab(),
        "old_mm5_sfclay": _run_old_mm5(),
        "gfs_sfclay": _run_gfs(),
    }
    overall = all(v["pass"] for v in schemes.values())
    return {
        "schema": "gpuwrf.v013.t3_surface_lsm_operational_fp64_oracle.v1",
        "tier": "v0.13 Tier-3 surface-layer + LSM (first batch)",
        "verdict": "PASS" if overall else "FAIL",
        "overall_pass": bool(overall),
        "oracle": {
            "type": "standalone single-column Fortran drivers linked against the "
                    "UNMODIFIED pristine WRF module_sf_{slab,sfclay,gfs}.F (NOT a JAX "
                    "self-compare); fp64 (-fdefault-real-8 -fdefault-double-8).",
            "savepoints": str(SP.relative_to(ROOT)),
            "drivers": "proofs/v013/oracle/surface_lsm/*_oracle_driver.f90 (+ *_build_and_run.sh)",
            "full_wrf_exe": False,
            "self_compare": False,
        },
        "jax_precision": "fp64",
        "jax_platform": jax.default_backend(),
        "git_head": _git_head(),
        "predeclared_tolerances": {"abs": ABS_TOL, "rel": REL_TOL, "relative_floor": REL_FLOOR},
        "schemes": schemes,
    }


def main() -> int:
    report = build_report()
    REPORT.parent.mkdir(parents=True, exist_ok=True)
    with REPORT.open("w", encoding="utf-8") as fh:
        json.dump(report, fh, indent=2, sort_keys=True)
        fh.write("\n")
    print(f"verdict={report['verdict']} -> {REPORT}")
    for name, sch in report["schemes"].items():
        worst = max(
            max(m["max_abs"] for m in c["fields"].values()) for c in sch["cases"]
        )
        print(f"  {name:18s} {'PASS' if sch['pass'] else 'FAIL'}  worst |jax-oracle|={worst:.3e}")
    return 0 if report["overall_pass"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
