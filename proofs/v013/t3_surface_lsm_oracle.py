ROOT = Path(__file__).resolve().parents[2]
SP = ROOT / "proofs" / "v013" / "savepoints" / "surface_lsm" / "fp64"
REPORT = Path(__file__).resolve().parent / "t3_surface_lsm_oracle.json"

# PREDECLARED fp64 operational-parity tolerances.
ABS_TOL = 5.0e-9
REL_TOL = 5.0e-10
REL_FLOOR = 1.0e-30

CASES = (1, 2, 3, 4, 5)


def _A(d):
    return jnp.asarray(d, jnp.float64)


def _metric(actual, expected):
    a = np.asarray(actual, dtype=np.float64)
    b = np.asarray(expected, dtype=np.float64)
    abs_err = np.abs(a - b)
    max_abs = float(np.max(abs_err))
    scale = max(float(np.max(np.abs(b))), REL_FLOOR)
    max_rel = max_abs / scale
    argmax = int(np.argmax(abs_err))
    return {
        "jax": float(np.ravel(a)[argmax]),
        "oracle": float(np.ravel(b)[argmax]),
        "max_abs": max_abs,
        "max_rel": float(max_rel),
        "pass": bool(max_abs <= ABS_TOL or max_rel <= REL_TOL),
    }


def _load(prefix: str, case: int) -> dict:
    with (SP / f"{prefix}_case_{case}.json").open(encoding="utf-8") as fh:
        return json.load(fh)


def _read_checksums(name: str) -> list[str]:
    p = SP / name
    return p.read_text(encoding="utf-8").splitlines() if p.exists() else []


# --------------------------------------------------------------------------- #
# Slab thermal-diffusion LSM (sf_surface_physics=1)                           #
# --------------------------------------------------------------------------- #
_SLAB_FIELDS = {
    "TSK": "tsk", "HFX": "hfx", "QFX": "qfx", "LH": "lh",
    "QSFC": "qsfc", "CHKLOWQ": "chklowq", "CAPG": "capg",
}


def _run_slab() -> dict:
    fn = jax.jit(slab_columns, static_argnames=("nsoil_steps", "ifsnow"))
    cases = []
    for c in CASES:
        d = _load("slab", c)
        col = d["columns"]; s = d["scalars"]; v = d["vectors"]
        zs = _A(v["ZS"]); dzs = _A(v["DZS"])
        tslb_in = _A(d["soil"]["TSLB_IN"])
        nss = nsoil_substeps(float(v["DZS"][0]), float(s["DELTSM"]))
        out = fn(
            _A(col["T"]), _A(col["QV"]), _A(col["P"]), _A(col["FLHC"]), _A(col["FLQC"]),
            _A(col["PSFC"]), _A(col["XLAND"]), _A(col["TSK_IN"]), tslb_in, _A(col["MAVAIL"]),
            _A(col["GSW"]), _A(col["GLW"]), _A(col["HFX_IN"]), _A(col["QFX_IN"]),
            jnp.zeros_like(_A(col["TSK_IN"])),   # lh_in (driver init LH=0)
            zs=zs, dzs=dzs, thc=_A(col["THC"]), tmn=_A(col["TMN"]),
            emiss=_A(col["EMISS"]), snowc=_A(col["SNOWC"]),
            deltsm=float(s["DELTSM"]), nsoil_steps=nss, ifsnow=int(s["IFSNOW"]),
        )
        out = {k: np.asarray(val) for k, val in out.items()}
        fields = {name: _metric(out[key], col[name]) for name, key in _SLAB_FIELDS.items()}
        fields["TSLB"] = _metric(out["tslb"], d["soil"]["TSLB"])
        case = {"case": c, "regime": s["REGIME"], "nsoil_steps": nss, "fields": fields}
        case["pass"] = bool(all(m["pass"] for m in fields.values()))
        cases.append(case)
    return {
        "scheme": "thermal-diffusion 5-layer slab LSM (sf_surface_physics=1)",
        "operational_kernel": "gpuwrf.physics.lsm_slab.slab_columns (jit/vmap-traceable)",
        "operational_status": (
            "REFERENCE_ONLY -- fp64 oracle-validated, but fail-closed in the operational "
            "scan until the slab LSM hook (TSLB land carry + GSW/GLW forcing + TMN/THC/EMISS "
            "statics) is threaded; selectable for a single-column reference comparison."
        ),
        "wrf_source": "/home/enric/src/wrf_pristine/WRF/phys/module_sf_slab.F",
        "source_checksums_sha256": _read_checksums("slab_wrf_source_checksums.txt"),
        "cases": cases,
        "pass": bool(all(c["pass"] for c in cases)),
    }


